package model;

public class DeviceVO {
	
	String p_id;
	String m_id;
	String user_id;
	String rental_date;
	String r_start_time;
	String r_return_time;
	
	
	public String getP_id() {
		return p_id;
	}
	public void setP_id(String p_id) {
		this.p_id = p_id;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getRental_date() {
		return rental_date;
	}
	public void setRental_date(String rental_date) {
		this.rental_date = rental_date;
	}
	public String getR_start_time() {
		return r_start_time;
	}
	public void setR_start_time(String r_start_time) {
		this.r_start_time = r_start_time;
	}
	public String getR_return_time() {
		return r_return_time;
	}
	public void setR_return_time(String r_return_time) {
		this.r_return_time = r_return_time;
	}
	
}
